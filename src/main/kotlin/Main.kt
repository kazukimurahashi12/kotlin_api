// package org.example

fun main() {
    // 変数var

    var a: Float
    a = 12345.6f
    println("a: $a")

    var t: String = "a"
    println(message = "t: $t")

    // 定数val
    val b: Int = 1000
    println("b: $b")
    val c = 10_000L
    println("c: $c")

    // 公式 Kotlin Tour
    // Hello world
    val name = "Mary"
    val age = 20
    println("$name is $age years old")

    // サフィックス
    // val pi = 3.14F
}
